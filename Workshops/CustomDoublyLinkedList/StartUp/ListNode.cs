namespace CustomDoublyLinkedList
{
    /// <summary>
    /// Element of Custom Double Linked List
    /// </summary>
    public class ListNode
    {
        /// <summary>
        /// Value of the Element
        /// </summary>
        public int Value { get; set; }
        /// <summary>
        /// Previous Element
        /// </summary>
        public ListNode Previous { get; set; }
        /// <summary>
        /// Next Element
        /// </summary>
        public ListNode Next { get; set; }
    }
}
